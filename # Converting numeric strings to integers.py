# Converting numeric strings to integers and floats
str_num_int = "42"
str_num_float = "3.14"

int_from_str = int(str_num_int)         # Converts "42" to 42
float_from_str = float(str_num_float)   # Converts "3.14" to 3.14

print(int_from_str)       # Output: 42
print(float_from_str)     # Output: 3.14
