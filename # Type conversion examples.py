# Type conversion examples
num_str = "42"
num_float = 3.14

# Converting string and float to integer
converted_from_string = int(num_str)   # Converts string "42" to integer 42
converted_from_float = int(num_float)   # Converts float 3.14 to integer 3

print(converted_from_string)  # Output: 42
print(converted_from_float)    # Output: 3
